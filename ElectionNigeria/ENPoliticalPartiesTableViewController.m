@interface ENPoliticalPartiesTableViewController ()
{
    NSArray *partiesList;
}

@end

@implementation ENPoliticalPartiesTableViewController

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    [self setUpViewController];
    partiesList = [NSArray arrayWithContentsOfFile:[[NSBundle mainBundle] pathForResource:@"PartiesList" ofType:@"plist"]];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return partiesList.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"partiesListCell" forIndexPath:indexPath];
    
    cell.textLabel.text = partiesList[indexPath.row];
    
    return cell;
}

